package com.learnersacademy.bean;

public class ClassBean {
	
	private int slNo;
	private String allocatedTeacher;
	private String allocatedSubject;
	private String allocatedClass;
	public int getSlNo() {
		return slNo;
	}
	public void setSlNo(int slNo) {
		this.slNo = slNo;
	}
	public String getAllocatedTeacher() {
		return allocatedTeacher;
	}
	public void setAllocatedTeacher(String allocatedTeacher) {
		this.allocatedTeacher = allocatedTeacher;
	}
	public String getAllocatedSubject() {
		return allocatedSubject;
	}
	public void setAllocatedSubject(String allocatedSubject) {
		this.allocatedSubject = allocatedSubject;
	}
	public String getAllocatedClass() {
		return allocatedClass;
	}
	public void setAllocatedClass(String allocatedClass) {
		this.allocatedClass = allocatedClass;
	}
	
	

}
